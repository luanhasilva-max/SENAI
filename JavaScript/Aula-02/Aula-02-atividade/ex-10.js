console.log(10 + 5);
// O operador + é usado para somar números, então o resultado será 15
console.log("10" + 5);
// O numero 10 nao é isto como um numero, mas sim como uma string, então o operador + é usado para concatenar strings, e o resultado será "105"
console.log(10 + "5");
// O numero 5 nao é isto como um numero, mas sim como uma string, então o operador + é usado para concatenar strings, e o resultado será "105"
console.log("10" + "5");
// os dois numeros são strings, então o operador + é usado para concatenar strings, e o resultado será "105"
